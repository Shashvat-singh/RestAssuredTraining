package A0602;

public class Popo {
	String name;
	int age;
	String grade;
	String[] subjects;
	
	public String setName() {
		return name;
	}
	
	public void getName() {
		this.name = name;
	}
	
	public int setAge() {
		return age;
	}
	
	public void getAge() {
		this.age = age;
	}
	
	public String setGrade() {
		return grade;
	}
	
	public void getGrade() {
		this.grade = grade;
	}
	
	public String[] setSubjects() {
		return subjects;
	}
	 
	public void getSubjects() {
		this.subjects = subjects;
	}

}
